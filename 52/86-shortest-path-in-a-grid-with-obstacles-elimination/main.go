func shortestPath(grid [][]int, k int) int {
	m := len(grid)
	n := len(grid[0])

	seen := map[[3]int]bool{}

	dirs := [][2]int{{1, 0}, {0, 1}, {-1, 0}, {0, -1}}
	work := [][4]int{{0, 0, 0, 0}}
	for len(work) > 0 {
		w := work[0]
		work = work[1:]
		key := [3]int{w[0], w[1], w[2]}
		if seen[key] {
			continue
		}
		seen[key] = true
		if w[0] == m-1 && w[1] == n-1 {
			return w[3]
		}
		for _, dir := range dirs {
			w2 := [4]int{w[0] + dir[0], w[1] + dir[1], w[2], w[3] + 1}
			if w2[0] < 0 || w2[0] >= m || w2[1] < 0 || w2[1] >= n {
				continue
			}
			if grid[w2[0]][w2[1]] == 1 {
				w2[2]++
			}
			if w2[2] > k {
				continue
			}
			work = append(work, w2)
		}
	}
	return -1
}
